package it.prisma.domain.dsl.paas.services.dnsaas;

/**
 * Request to create a new DN associated to a PaaSService.
 */
public class PaaSServiceDNRequest {
	private Long paasServiceId;
	private Long dnsZoneId;
	private String domainName;
	
	
	
	/**
	 * 
	 */
	public PaaSServiceDNRequest() {
		super();
	}

	/**
	 * @param paasServiceId
	 * @param dnsZoneId
	 * @param domainName
	 */
	public PaaSServiceDNRequest(Long paasServiceId, Long dnsZoneId, String domainName) {
		super();
		this.paasServiceId = paasServiceId;
		this.dnsZoneId = dnsZoneId;
		this.domainName = domainName;
	}

	public Long getPaasServiceId() {
		return paasServiceId;
	}

	public void setPaasServiceId(Long paasServiceId) {
		this.paasServiceId = paasServiceId;
	}

	public Long getDnsZoneId() {
		return dnsZoneId;
	}

	public void setDnsZoneId(Long dnsZoneId) {
		this.dnsZoneId = dnsZoneId;
	}

	public String getDomainName() {
		return domainName;
	}

	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

}
